package numbersProgram;

public class A_23_24_Square_SquareRoot 
{
	public static void main(String[] args) 
	{
		System.out.println("Prg 23-->Java Program to Find Square Root of a Number");
		int a=9;
		for(int b=1; b<=a; b++)
		{
			if(b*b==a)
			{
				System.out.println("Square Root of " +a+ " is " +b);
			}
		}
		System.out.println();
		System.out.println("Prg 24 -Java Program to Find Square of given Number");
		int c=7;
		System.out.println("Square of "+c+ " is "+ (c*c));
		
	}

}
